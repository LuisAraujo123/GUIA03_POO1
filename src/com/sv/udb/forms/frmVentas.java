    private void poblarData()
    {
        //Cambiar color de selecciÃ³n
        this.tblVentas.setSelectionBackground(new Color(255, 255, 204));
        this.tblVentas.setSelectionForeground(Color.BLACK);
        
        DefaultTableModel model = (DefaultTableModel)this.tblVentas.getModel();
        while(model.getRowCount()>0){model.removeRow(0);} //Limpiar modelo

        //Obtenes y cargar la data CSV
        ventas obje = new ventas();
        lstRegi = new ArrayList<>();
        this.lstRegi = obje.getDataRegistros();
        for(Registro temp : this.lstRegi)
        {
            model.addRow(temp.verVentas());
        }
    }
    
    private String fechaSist() {
        Calendar c1 = new GregorianCalendar();
        String dia, mes, anio;
        dia = String.valueOf(c1.get(Calendar.DATE));
        mes =  String.valueOf(c1.get(Calendar.MONTH));
        anio = String.valueOf(c1.get(Calendar.YEAR));
        if (dia.length() == 1){
            dia = "0"+dia;
        }
        if (mes.length() == 1){
            mes = "0"+mes;
        }
            String fecha = dia + "/" + mes + "/" + anio;
            return fecha;
    }
    
    private void Filtrar(boolean op) {
        DefaultTableModel model = (DefaultTableModel)this.tblVentas.getModel();
        while(model.getRowCount()>0){model.removeRow(0);} //Limpiar modelo
        //Se filtra los datos
        for(Registro temp : this.lstRegi)
        {
            if (op == false)
            {
                model.addRow(temp.verVentas());
            }
            else
            {
                if (temp.getFecha().equals(fechaSist()))
                {
                    model.addRow(temp.verVentas());
                }
            }
        }
    }

